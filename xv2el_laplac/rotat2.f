
c**********************************************************************
      subroutine rotat2(x0,x,a)
c**********************************************************************
      double precision x0(3),x(3),a,c,s
      c=cos(a)
      s=sin(a)
      x(1)=x0(1)*c-x0(3)*s
      x(2)=x0(2)
      x(3)=x0(1)*s+x0(3)*c
      end

